#!/usr/bin/env python3

"""
    Add function that takes two parameters
    a - float
    b - float
    Return float
"""


def add(a: float, b: float) -> float:
    """
        Add two float numbers and return the result.

        Parameters:
        a (float): The first input float number.
        b (float): The second input float number.

        Returns:
        float: The sum of the two input numbers.
    """
    return a + b
